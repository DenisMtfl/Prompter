create table if not exists public."PROMPT_preset_tags" (
    preset_id text not null references public."PROMPT_presets" (id) on delete cascade,
    tag_id integer not null references public."PROMPT_tags" (id) on delete cascade,
    language_code text not null check (language_code in ('de', 'en')),
    sort_order smallint not null,
    primary key (preset_id, tag_id),
    constraint prompt_preset_tags_order_unique unique (preset_id, language_code, sort_order)
);

