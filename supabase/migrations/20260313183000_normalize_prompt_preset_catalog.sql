create table if not exists public."PROMPT_categories" (
    id integer generated by default as identity primary key,
    code text not null unique
);

create table if not exists public."PROMPT_main_categories" (
    id integer generated by default as identity primary key,
    code text not null unique
);

create table if not exists public."PROMPT_platforms" (
    id integer generated by default as identity primary key,
    name text not null unique
);

create table if not exists public."PROMPT_tones" (
    id integer generated by default as identity primary key,
    code text not null unique
);

create table if not exists public."PROMPT_lengths" (
    id integer generated by default as identity primary key,
    code text not null unique
);

