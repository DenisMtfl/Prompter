create view public."PROMPT_preset_catalog_flat" as
select
    preset.id,
    preset.slug,
    category_lookup.code as category,
    preset.title_de,
    preset.title_en,
    preset.description_de,
    preset.description_en,
    subcategory_lookup.name_de as subcategory_de,
    subcategory_lookup.name_en as subcategory_en,
    coalesce(array_agg(tag_lookup.value order by preset_tag.sort_order) filter (where preset_tag.language_code = 'de'), array[]::text[]) as tags_de,
    coalesce(array_agg(tag_lookup.value order by preset_tag.sort_order) filter (where preset_tag.language_code = 'en'), array[]::text[]) as tags_en,
    preset.popularity_score,
    preset.example_input_de,
    preset.example_input_en,
    preset.example_prompt_de,
    preset.example_prompt_en,
    main_category_lookup.code as default_main_category,
    preset.default_topic_de,
    preset.default_topic_en,
    subcategory_lookup.name_de as default_subcategory_de,
    subcategory_lookup.name_en as default_subcategory_en,
    platform_lookup.name as default_platform,
    preset.default_target_audience_de,
    preset.default_target_audience_en,
    preset.default_role_de,
    preset.default_role_en,
    preset.default_extra_instructions_de,
    preset.default_extra_instructions_en,
    tone_lookup.code as default_tone,
    length_lookup.code as default_length,
    output_format_lookup.code as default_output_format,
    complexity_lookup.code as default_complexity_level,
    preset.is_generated,
    preset.created_at,
    preset.updated_at
from public."PROMPT_presets" preset
join public."PROMPT_categories" category_lookup on category_lookup.id = preset.category_id
join public."PROMPT_subcategories" subcategory_lookup on subcategory_lookup.id = preset.subcategory_id
join public."PROMPT_main_categories" main_category_lookup on main_category_lookup.id = preset.main_category_id
join public."PROMPT_platforms" platform_lookup on platform_lookup.id = preset.platform_id
