do $$
declare
    table_name text;
    policy_name text;
    prompt_tables text[] := array[
        'PROMPT_categories',
        'PROMPT_main_categories',
        'PROMPT_platforms',
        'PROMPT_tones',
        'PROMPT_lengths',
        'PROMPT_output_formats',
        'PROMPT_complexity_levels',
        'PROMPT_subcategories',
        'PROMPT_tags',
        'PROMPT_presets',
        'PROMPT_preset_tags'
    ];
begin
    foreach table_name in array prompt_tables loop
        execute format('alter table public.%I enable row level security', table_name);
        execute format('grant select on public.%I to anon, authenticated', table_name);

        policy_name := table_name || '_select_public';
        if not exists (
            select 1
            from pg_policies
            where schemaname = 'public'
              and tablename = table_name
              and policyname = policy_name
        ) then
            execute format(
                'create policy %I on public.%I for select to anon, authenticated using (true)',
                policy_name,
                table_name);
        end if;
    end loop;
end $$;

