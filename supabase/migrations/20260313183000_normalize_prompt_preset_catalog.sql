join public."PROMPT_lengths" length_lookup on length_lookup.id = preset.length_id
join public."PROMPT_output_formats" output_format_lookup on output_format_lookup.id = preset.output_format_id
join public."PROMPT_complexity_levels" complexity_lookup on complexity_lookup.id = preset.complexity_level_id
left join public."PROMPT_preset_tags" preset_tag on preset_tag.preset_id = preset.id
left join public."PROMPT_tags" tag_lookup on tag_lookup.id = preset_tag.tag_id
group by
    preset.id,
