);

create table if not exists public."PROMPT_complexity_levels" (
    id integer generated by default as identity primary key,
    code text not null unique
);

create table if not exists public."PROMPT_subcategories" (
    id integer generated by default as identity primary key,
    category_id integer not null references public."PROMPT_categories" (id) on delete cascade,
    name_de text not null,
    name_en text not null,
    constraint prompt_subcategories_unique unique (category_id, name_de, name_en)
);

create table if not exists public."PROMPT_tags" (
    id integer generated by default as identity primary key,
    language_code text not null check (language_code in ('de', 'en')),
    value text not null,
    normalized_value text generated always as (lower(value)) stored,
    constraint prompt_tags_unique unique (language_code, normalized_value)
);

create table if not exists public."PROMPT_presets" (
    id text primary key,
    slug text not null unique,
    category_id integer not null references public."PROMPT_categories" (id),
    subcategory_id integer not null references public."PROMPT_subcategories" (id),
